public class Calculator {

    public Calculator() {
    }

    public int addNumbers(int i, int i1) {
        return i + i1;
    }


    public int subtractNumbers(int i, int i1) {
        return i - i1;
    }

    public int multiplyNumbers(int i, int i1) {
        return i * i1;
    }

    public double divideNumbers(double v, double v1) {
        return v / v1;
    }
}
